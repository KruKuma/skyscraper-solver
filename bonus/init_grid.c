void	init_grid(int grid[9][9], int size)
{
	int	row;
	int	col;

	row = 0;
	while (row < size)
	{
		col = 0;
		while (col < size)
		{
			grid[row][col] = 0;
			col++;
		}
		row++;
	}
}
